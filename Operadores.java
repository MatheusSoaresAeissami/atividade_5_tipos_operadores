public class Operadores {
    // public class Operadores {
    public float adicao(float v1, float v2) {
        return v1 + v2;
    }
    
    public float subtracao(float v1, float v2) {
        return v1 - v2;
    }
    
    public float multiplicacao(float v1, float v2) {
        return v1 * v2;
    }
    
    public float divisao(float v1, float v2) {
        return v1 / v2;
    }
    
    public float atribuicao(float v1, float v2) {
        return v1 = v2;
    }
    
    public boolean igualdade(float v1, float v2) {
        return v1 == v2;
    }
    
    public boolean diferenca(float v1, float v2) {
        return v1 != v2;
    }
    
    public boolean maiorQue(float v1, float v2) {
        return v1 > v2;
    }
    
    public boolean menorQue(float v1, float v2) {
        return v1 < v2;
    }
    
    public boolean maiorOuIgualQue(float v1, float v2) {
        return v1 >= v2;
    }
    
    public boolean menorOuIgualQue(float v1, float v2) {
        return v1 <= v2;
    }
    
    public boolean and(boolean v1, boolean v2) {
        return v1 && v2;
    }
    
    public boolean or(boolean v1, boolean v2) {
        return v1 || v2;
    }
}
